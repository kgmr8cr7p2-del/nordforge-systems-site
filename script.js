const html = document.documentElement;
const langButtons = document.querySelectorAll('.lang-btn');
const translatableNodes = document.querySelectorAll('[data-i18n]');
const menuToggle = document.getElementById('menuToggle');
const mainNav = document.getElementById('mainNav');
const revealNodes = document.querySelectorAll('.reveal');

function applyLanguage(lang) {
  const dict = translations[lang] || translations.ru;

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dict[key]) {
      node.textContent = dict[key];
    }
  });

  html.lang = lang;
  document.title = lang === 'en'
    ? 'NORDFORGE SYSTEMS — Ready PCs and complete solutions'
    : 'NORDFORGE SYSTEMS — Готовые ПК и комплексные решения';

  const description = document.querySelector('meta[name="description"]');
  if (description) {
    description.setAttribute(
      'content',
      lang === 'en'
        ? 'Ready-built PCs, custom assembly, software installation and setup, complete turnkey system preparation.'
        : 'Готовые ПК, сборка под задачу, установка и настройка ПО, комплексная подготовка систем под ключ.'
    );
  }

  langButtons.forEach((button) => {
    button.classList.toggle('active', button.dataset.lang === lang);
  });

  localStorage.setItem('nordforge-language', lang);
}

langButtons.forEach((button) => {
  button.addEventListener('click', () => applyLanguage(button.dataset.lang));
});

const savedLanguage = localStorage.getItem('nordforge-language') || 'ru';
applyLanguage(savedLanguage);

menuToggle?.addEventListener('click', () => {
  mainNav.classList.toggle('open');
  document.body.classList.toggle('nav-open');
});

document.querySelectorAll('.nav a').forEach((link) => {
  link.addEventListener('click', () => {
    mainNav.classList.remove('open');
    document.body.classList.remove('nav-open');
  });
});

const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add('is-visible');
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.12 });

revealNodes.forEach((node) => revealObserver.observe(node));
